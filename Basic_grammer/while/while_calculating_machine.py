i = 0

while(i == 0):
    q = int(input())
    a = int(input())
    b = int(input())
    if(q == 1):
        print(a + b)
    elif(q == 2):
        print(a - b)
    elif(q == 3):
        print(a * b)
    elif(q == 4):
        print(a / b)
    elif(q == 5):
        print(a % b)
    elif(q == 6):
        break